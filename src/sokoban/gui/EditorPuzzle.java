    private void btnSavePuzzleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSavePuzzleActionPerformed
        // Save button
        String fileName = JOptionPane.showInputDialog("Dá um nome ao teu mapa:");
        if (fileName == null || fileName.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Dá um nome ao teu mapa:");
            return;
        }
        File pasta = new File("./PuzzlesEditor");
        File file = new File(pasta, fileName + ".txt");
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            writer.write(sokobanEditor1.toString());
        } catch (IOException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSavePuzzleActionPerformed

