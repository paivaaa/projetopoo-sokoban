    private void sokobanEditor1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sokobanEditor1KeyPressed
        // Teclas: 
        if (evt.getKeyCode() == KeyEvent.VK_LEFT) {
            sokobanEditor1.movePlayer(-1, 0, 1);
            
        } else if (evt.getKeyCode() == KeyEvent.VK_RIGHT) {
            sokobanEditor1.movePlayer(1, 0, 1);
            
        } else if (evt.getKeyCode() == KeyEvent.VK_UP) {
            sokobanEditor1.movePlayer(0, -1, 1);
            
        } else if (evt.getKeyCode() == KeyEvent.VK_DOWN) {
            sokobanEditor1.movePlayer(0, 1, 1);
      
        }
    }//GEN-LAST:event_sokobanEditor1KeyPressed

