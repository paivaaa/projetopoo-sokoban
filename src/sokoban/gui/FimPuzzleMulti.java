    private void btnNextLevelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextLevelActionPerformed
        // Botão proximo nivel:
        puzzle = jogoMulti1.getNumeroPuzzle();
        proxPuzzle = puzzle + 1;
        sokobanMulti.apagarPuzzle();
        jogoMulti1.setPuzzle(PuzzlesMulti.getPuzzleMulti(proxPuzzle));
        jogoMulti1.atualizarNivelPuzzle();
        dispose(); // Fecha a janela atual 
        // Abre uma nova instância de JogoSingle com o novo puzzle
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                jogoMulti1.setVisible(true);

            }

        });

    }//GEN-LAST:event_btnNextLevelActionPerformed

    private void btnRestartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestartActionPerformed
        // Botão restart:
        jogoMulti1.restartstats();
        sokobanMulti.apagarPuzzle();
        sokobanMulti.setPuzzle(jogoMulti1.guardarPuzzle);
        dispose();
    }//GEN-LAST:event_btnRestartActionPerformed

    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        //Botão ao menu:
        MenuMultiPlayer menuMulti = new MenuMultiPlayer();
        menuMulti.setLocationRelativeTo(null);
        menuMulti.setVisible(true);

        dispose();
    }//GEN-LAST:event_btnExitActionPerformed

